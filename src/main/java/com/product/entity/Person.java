public class Person {

    private String phone;

    private String centerUserId;

    private String tenanid;

    private String msg;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCenterUserId() {
        return centerUserId;
    }

    public void setCenterUserId(String centerUserId) {
        this.centerUserId = centerUserId;
    }

    public String getTenanid() {
        return tenanid;
    }

    public void setTenanid(String tenanid) {
        this.tenanid = tenanid;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
